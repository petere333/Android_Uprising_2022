CFbxScene::CFbxScene(FbxScene* pfbxScene)
{
	m_pfbxScene = pfbxScene;
}

CFbxScene::~CFbxScene()
{
	if (m_pfbxScene) ::ReleaseRenderInfoFromFbxNodeHierarchy(m_pfbxScene->GetRootNode());
	if (m_pfbxScene) m_pfbxScene->Destroy();
}
void CMonsterObject::Animate(float fTimeElapsed)
{
	if (m_pAnimationController) m_pAnimationController->AdvanceTime(fTimeElapsed);
}
void CMonsterObject::Render(ID3D12GraphicsCommandList* pd3dCommandList)
{
	OnPrepareRender();

	if (m_pFbxScene->m_pfbxScene)
	{
		FbxAMatrix fbxf4x4World = ::XmFloat4x4MatrixToFbxMatrix(m_xmf4x4World);
		FbxTime fbxCurrentTime = m_pAnimationController->GetCurrentTime();
		::RenderFbxNodeHierarchy(pd3dCommandList, m_pFbxScene->m_pfbxScene->GetRootNode(), fbxCurrentTime, fbxf4x4World, m_nInstance);
	}
}